    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
         
       String nom = na.getText();
       String cor = co.getText();
       String num = nu.getText();
       
       name.add(na);
       mail.add(co);
       phone.add(nu);
       na.setText("");
       co.setText("");
       nu.setText("");
       nu.requestFocus();
       row[0] = nom;
       row[1] = cor;
       row[2] = num;
       
       Tabla.addRow(row);
       message.setText("Se ha agregado un contacto!");
       
    }//GEN-LAST:event_addActionPerformed

    private void eraseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eraseActionPerformed
         
      int a = table.getSelectedRow();
      if(a >=0){
          Tabla.removeRow(a);
          message.setText("Se ha eliminado un contacto!");
      }
       
    }//GEN-LAST:event_eraseActionPerformed

